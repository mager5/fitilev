// Функция для получения данных статьи по id
function getBlogPostById(id: string) {
  return blogPosts.find(post => post.id === id);
}

// Генерация статических путей для всех статей блога
export function generateStaticParams() {
  return blogPosts.map(post => ({
    id: post.id,
  }));
}

// Генерация метаданных для каждой статьи
export async function generateMetadata({ params }: { params: { id: string } }) {
  const post = getBlogPostById(params.id);
  
  if (!post) {
    return {
      title: 'Статья не найдена | Блог Алексея Фитиля',
      description: 'Запрашиваемая статья не найдена'
    };
  }
  
  return {
    title: `${post.title} | Блог Алексея Фитиля`,
    description: post.description,
    keywords: `фитнес, ${post.category.toLowerCase()}, здоровье, тренировки, ${post.title.toLowerCase()}`
  };
}

